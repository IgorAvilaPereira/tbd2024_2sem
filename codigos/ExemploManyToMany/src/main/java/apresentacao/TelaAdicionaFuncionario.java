    private void renderizaList() {
        this.list1.removeAll();
        this.list1.repaint();
           List<Funcionario> vetFuncionario = this.entityManager.createQuery("SELECT f FROM Funcionario f").getResultList();
        for (int i = 0; i < vetFuncionario.size(); i++) {
            Funcionario g = vetFuncionario.get(i);
            this.list1.add(g.toString());
            
        }
    }
