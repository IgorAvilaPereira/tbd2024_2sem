    private void list1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_list1ActionPerformed
        // TODO add your handling code here:
        List<Funcionario> vetFuncionario = this.entityManager.createQuery("SELECT f FROM Funcionario f").getResultList();
        new FuncionarioDAO(entityManager).excluir(vetFuncionario.get(this.list1.getSelectedIndex()).getId());
        JOptionPane.showMessageDialog(this, "parabens", "ok", JOptionPane.INFORMATION_MESSAGE);
        this.renderizaList();
    }//GEN-LAST:event_list1ActionPerformed

    private void list1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list1MouseClicked
        // TODO add your handling code here:
        Long id = Long.valueOf(JOptionPane.showInputDialog("Digite o id do projeto"));
        Projeto p = new ProjetoDAO(entityManager).obter(id);        
        List<Funcionario> vetFuncionario = this.entityManager.createQuery("SELECT f FROM Funcionario f").getResultList();
        Funcionario f =  vetFuncionario.get(this.list1.getSelectedIndex());
        
        new FuncionarioDAO(entityManager).adicionaFuncionarioAoProjeto(f, p);
        
        JOptionPane.showMessageDialog(this, "feito!!");
    }//GEN-LAST:event_list1MouseClicked

